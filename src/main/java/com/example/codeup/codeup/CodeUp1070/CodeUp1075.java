import java.util.Scanner;

public class CodeUp1075 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int count = sc.nextInt();
        for(int i = count - 1; i >= 0; i--)
            System.out.println(i);
    }
}
